/** @format */

/**
 * A channel of a color
 */
export enum Channel {
  /**
   * Red channel
   */
  red,
  /**
   * Green channel
   */
  green,
  /**
   * Blue channel
   */
  blue,
  /**
   * Alpha channel
   */
  alpha,
  /**
   * Luminance is not an actual channel, it is the brightness value of the color.
   */
  luminance,
}
