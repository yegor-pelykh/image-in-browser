/** @format */

/**
 * Enum representing different color channels.
 */
export enum Channel {
  /**
   * Red color channel.
   */
  red,
  /**
   * Green color channel.
   */
  green,
  /**
   * Blue color channel.
   */
  blue,
  /**
   * Alpha (transparency) channel.
   */
  alpha,
  /**
   * Luminance channel.
   */
  luminance,
}
