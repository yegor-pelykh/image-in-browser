/** @format */

/**
 * Enum representing different types of noise.
 */
export enum NoiseType {
  /**
   * Gaussian noise.
   */
  gaussian,

  /**
   * Uniform noise.
   */
  uniform,

  /**
   * Salt and pepper noise.
   */
  saltAndPepper,

  /**
   * Poisson noise.
   */
  poisson,

  /**
   * Rice noise.
   */
  rice,
}
