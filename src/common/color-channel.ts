/** @format */

export enum ColorChannel {
  /**
   * Red channel of a color.
   */
  red,

  /**
   * Green channel of a color.
   */
  green,

  /**
   * Blue channel of a color.
   */
  blue,

  /**
   * Alpha channel of a color.
   */
  alpha,

  /**
   * Luminance (brightness) of a color.
   */
  luminance,
}
