/** @format */

/**
 * Enum representing various image formats.
 */
export enum ImageFormat {
  /** Bitmap image format */
  bmp,
  /** Graphics Interchange Format */
  gif,
  /** Icon image format */
  ico,
  /** JPEG image format */
  jpg,
  /** Portable Network Graphics */
  png,
  /** Portable Any Map */
  pnm,
  /** Photoshop Document */
  psd,
  /** PowerVR texture format */
  pvr,
  /** Truevision TGA */
  tga,
  /** Tagged Image File Format */
  tiff,
  /** WebP image format */
  webp,
  /** Custom image format */
  custom,
  /** Invalid image format */
  invalid,
}
