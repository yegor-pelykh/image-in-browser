/** @format */

/**
 * Enum representing different color modes in PSD files.
 */
export enum PsdColorMode {
  /** Bitmap color mode */
  bitmap,
  /** Grayscale color mode */
  grayscale,
  /** Indexed color mode */
  indexed,
  /** RGB color mode */
  rgb,
  /** CMYK color mode */
  cmyk,
  /** Multi-channel color mode */
  multiChannel,
  /** DuoTone color mode */
  duoTone,
  /** Lab color mode */
  lab,
}
